    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        try {
            harga = Integer.valueOf(txt_harga.getText());
            hari_sewa = Integer.valueOf(txt_jumlah.getText());
            total = hari_sewa * harga;
            java.sql.Date tgl = new java.sql.Date(jDateChooser1.getDate().getTime());
            
            String nomorPolisi = txt_NomorPolisi.getText();

    
    if (!isMobilTersedia(nomorPolisi)) {
        JOptionPane.showMessageDialog(null, "Mobil dengan nomor polisi " + nomorPolisi + " tidak tersedia.");
        return;
    }

            
            Peminjaman pemin=new Peminjaman(Integer.valueOf(txt_IdPegawai.getText()),txt_NomorPolisi.getText(), 
                   Integer.valueOf(txt_IdCustomer.getText()), tgl, harga, hari_sewa, total);
            
            
            
           PeminjamanManager pm = new PeminjamanManager();
           
           int i=pm.simpanData(pemin); 
           if (i==1)
           {
               JOptionPane.showMessageDialog(null, "Simpan Data Berhasil");
               
               updateStatusMobil(txt_NomorPolisi.getText(), "Tidak Tersedia");
               
               clear();
               txt_IdTransaksi.requestFocus();
               update();
           }
           else
           {
              JOptionPane.showMessageDialog(null, "Data Gagal Disimpan"); 
              txt_IdTransaksi.requestFocus();
           }
        }catch(Exception e){
             e.printStackTrace();
    JOptionPane.showMessageDialog(null, "Terjadi Kesalahan: " + e.getMessage());
        }
    }//GEN-LAST:event_btn_simpanActionPerformed

    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        try {
        if(txt_IdTransaksi.getText().equals("") || txt_IdCustomer.getText().equals("") || txt_NomorPolisi.getText().equals("") || txt_harga.getText().equals("") || txt_jumlah.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Pilih data!");
        } else {
            int jawab = JOptionPane.showConfirmDialog(rootPane, "Hapus "+txt_IdTransaksi.getText()+" ?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if(jawab==JOptionPane.YES_OPTION) {
                java.sql.Date tgl = new java.sql.Date(jDateChooser1.getDate().getTime());
                Peminjaman pm = new Peminjaman(Integer.valueOf(txt_IdTransaksi.getText()),Integer.valueOf(txt_IdPegawai.getText()), txt_NomorPolisi.getText(), Integer.valueOf(txt_IdCustomer.getText()), tgl, harga, hari_sewa, total);
                PeminjamanManager st = new PeminjamanManager();
                if(st.hapusData(pm) == 1) {
                    JOptionPane.showMessageDialog(this, "Berhasil hapus data");
                   txt_IdTransaksi.requestFocus();
                   update();
                   clear();
               } else {
                   JOptionPane.showMessageDialog(this, "Transaksi sudah selesai");
               }
           }
        }
        } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Masukkan data yang benar!");
        }
    }//GEN-LAST:event_btn_hapusActionPerformed

