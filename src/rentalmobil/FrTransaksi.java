    private void updateStatusMobil(String nomorPolisi, String status) {
    String sql = "UPDATE mobil SET status = ? WHERE nomor_polisi = ?";
    PreparedStatement stat = null;

    try {
        stat = _Cnn.prepareStatement(sql);
        stat.setString(1, status);
        stat.setString(2, nomorPolisi);

        int rowsUpdated = stat.executeUpdate();
        if (rowsUpdated > 0) {
            System.out.println("Status mobil berhasil diperbarui.");
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Gagal Memperbarui Status Mobil: " + e.getMessage());
    } finally {
        try {
            if (stat != null) stat.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
}
    private boolean isMobilTersedia(String nomorPolisi) {
    String sql = "SELECT status FROM mobil WHERE nomor_polisi = ?";
    PreparedStatement stat = null;
    ResultSet rs = null;

    try {
        stat = _Cnn.prepareStatement(sql);
        stat.setString(1, nomorPolisi);
        rs = stat.executeQuery();

        if (rs.next()) {
            String status = rs.getString("status");
            return status.equalsIgnoreCase("Tersedia");
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Gagal Memeriksa Status Mobil: " + e.getMessage());
    } finally {
        try {
            if (rs != null) rs.close();
            if (stat != null) stat.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    return false;
}


